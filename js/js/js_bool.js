// truthy: boolean으로 타입을 변환했을 때, true가 되는 값
// falsy: boolean으로 타입을 변환했을 때, false가 되는 값

// truthy
console.log(`true: ${!!true}`);
console.log(`23: ${!!23}`);
console.log(`hello: ${!!"hello"}`);

// falsy
console.log(`false: ${!!false}`);
console.log(`0: ${!!0}`);
console.log(`"": ${!!""}`);
console.log(`null: ${!!null}`);
console.log(`undefined: ${!!undefined}`);
